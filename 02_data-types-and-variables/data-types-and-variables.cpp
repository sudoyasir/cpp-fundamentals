#include <iostream>
using namespace std;

int main() {
    // Different types of variables
    int myInt = 25;              // Integer
    float myFloat = 3.14;         // Floating point number
    double myDouble = 9.81;       // Double precision floating point number
    char myChar = 'A';            // Character
    bool myBool = true;           // Boolean (true or false)
    string myString = "Hello";    // String

    // Display variables
    cout << "Integer: " << myInt << endl;
    cout << "Float: " << myFloat << endl;
    cout << "Double: " << myDouble << endl;
    cout << "Character: " << myChar << endl;
    cout << "Boolean: " << myBool << endl;
    cout << "String: " << myString << endl;

    return 0;
}
